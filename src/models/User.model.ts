import mongoose from "mongoose";
import ProductModel from "./Product.model";

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  role: {
    type: String,
    enum: ["admin", "user"],
    default: "user",
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  itemsBought: {
    type: [ProductModel.schema],
    default: [],
  },
});

export default mongoose.model("User", UserSchema);